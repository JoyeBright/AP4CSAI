# Define a new class named MyFirstClass
class MyFirstClass:
    # pass means the class has no content yet
    # i.e., an empty placeholder
    pass

# Create an object (instance) of MyFirstClass
a = MyFirstClass()

# Create another object (a separate instance)
b = MyFirstClass()

# Print object a
# Python shows the class name and memory address
print(a)

# Print object b
# This is a different object, so it has a different address
print(b)

